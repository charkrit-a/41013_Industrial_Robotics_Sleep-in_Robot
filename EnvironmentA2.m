function EnvironmentA2()    
    % ENVIRONMENTA2 renders the environment for assessment2

    % surfaces
    % floor
    surf([-2,-2;2,2] ...
    ,[-2,2;-2,2] ...
    ,[0.01,0.01;0.01,0.01] ...
    ,'CData',imread('floor.jpg') ...
    ,'FaceColor','texturemap');
    % wall
    surf([-2,-2;-2,-2] ...
    ,[-2,-2;2,2] ...
    ,[3,0.01;3,0.01] ...
    ,'CData',permute(imread('wall.jpg'),[2 1 3]) ...
    ,'FaceColor','texturemap');

    % objects
    % barrier
    PlaceObject('ExtendableGate.ply', ...
    [ 0.1 -2 0; -1.4 -2 0; ...
    0.1 2 0; -1.4 2 0;]);
    % operator
    PlaceObject('Worker.ply', ...
    [ 1.9 1 0.01 ]);
    % fire extinguisher
    PlaceObject('FireExtinguisherCO2.ply', ...
    [ 1.9 1.6 0.01 ]);

    % signs
end